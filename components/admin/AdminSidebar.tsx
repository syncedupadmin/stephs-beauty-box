'use client';

/**
 * Admin Sidebar
 * =============
 * Navigation sidebar matching editorial aesthetic
 */

import Link from 'next/link';
import { usePathname } from 'next/navigation';
import { useState } from 'react';

const navigation = [
  { name: 'Dashboard', href: '/admin', icon: DashboardIcon },
  { name: 'Services', href: '/admin/services', icon: ServicesIcon },
  { name: 'Products', href: '/admin/products', icon: ProductsIcon },
  { name: 'Bookings', href: '/admin/bookings', icon: BookingsIcon },
  { name: 'Orders', href: '/admin/orders', icon: OrdersIcon },
  { name: 'Settings', href: '/admin/settings', icon: SettingsIcon },
];

export function AdminSidebar() {
  const pathname = usePathname();
  const [mobileOpen, setMobileOpen] = useState(false);

  const isActive = (href: string) => {
    if (href === '/admin') {
      return pathname === '/admin';
    }
    return pathname.startsWith(href);
  };

  return (
    <>
      {/* Mobile menu button */}
      <button
        onClick={() => setMobileOpen(true)}
        className="lg:hidden fixed top-4 left-4 z-40 p-2 bg-charcoal text-paper"
        aria-label="Open menu"
      >
        <MenuIcon />
      </button>

      {/* Mobile backdrop */}
      {mobileOpen && (
        <div
          className="lg:hidden fixed inset-0 z-40 bg-charcoal/60 backdrop-blur-sm"
          onClick={() => setMobileOpen(false)}
        />
      )}

      {/* Sidebar */}
      <aside
        className={`fixed inset-y-0 left-0 z-50 w-64 bg-charcoal transform transition-transform duration-600 ease-luxury lg:translate-x-0 ${
          mobileOpen ? 'translate-x-0' : '-translate-x-full'
        }`}
      >
        <div className="flex flex-col h-full">
          {/* Brand */}
          <div className="flex items-center justify-between h-16 px-6 border-b border-paper/10">
            <Link href="/admin" className="font-display text-lg text-paper">
              Admin
            </Link>
            <button
              onClick={() => setMobileOpen(false)}
              className="lg:hidden text-paper/60 hover:text-paper transition-colors duration-600"
              aria-label="Close menu"
            >
              <CloseIcon />
            </button>
          </div>

          {/* Navigation */}
          <nav className="flex-1 px-3 py-6 space-y-1 overflow-y-auto">
            {navigation.map((item) => {
              const active = isActive(item.href);
              return (
                <Link
                  key={item.name}
                  href={item.href}
                  onClick={() => setMobileOpen(false)}
                  className={`flex items-center gap-3 px-3 py-3 text-body-sm font-body transition-all duration-600 ${
                    active
                      ? 'bg-botanical text-paper'
                      : 'text-paper/60 hover:text-paper hover:bg-paper/5'
                  }`}
                >
                  <item.icon className="w-5 h-5" />
                  {item.name}
                </Link>
              );
            })}
          </nav>

          {/* Footer */}
          <div className="px-6 py-4 border-t border-paper/10">
            <Link
              href="/"
              target="_blank"
              className="text-body-sm text-paper/40 hover:text-paper/60 transition-colors duration-600 font-body"
            >
              View Site â†’
            </Link>
          </div>
        </div>
      </aside>
    </>
  );
}

